public class BasicVariable4 {
     public static void main(String[] args) {
		
		int angka1 = 10;
		int angka2 = 20;
		angka1 = angka1 + 1;
		angka2 = angka2 - 1;
		System.out.println(angka1);
		System.out.println(angka2);
		 angka1 = 10;
		 angka2 = 20;
		angka1++;
		angka2--;
		System.out.println(angka1);
		System.out.println(angka2);
    }
}
